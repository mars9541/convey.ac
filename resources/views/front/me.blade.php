    $('#send_message').click(function() {
        var error_flag = 0;

        $('.loading-show2').show();
        $('#send_message').prop('disabled', true);

        if($('#employer_name').val() == "") {
            $('#employer_name').css('box-shadow', '0px 0px 4px red');
            $('#employer_name').css('margin-bottom', '0px');
            $('#employer_name_error').show();
            error_flag = 1;
        } else {
            $('#employer_name').css('box-shadow', '');
            $('#employer_name').css('margin-bottom', '');
            $('#employer_name_error').hide();
        }

        if($('#employer_website').val() == "") {
            $('#employer_website').css('box-shadow', '0px 0px 4px red');
            $('#employer_website').css('margin-bottom', '0px');
            $('#employer_website_error').show();
            error_flag = 1;
        } else {
            $('#employer_website').css('box-shadow', '');
            $('#employer_website').css('margin-bottom', '');
            $('#employer_website_error').hide();
        }

        if(error_flag == 1) {
            $('.loading-show2').hide();
            $('#send_message').prop('disabled', false);

            return false;
        }

        setTimeout(function() {
            alertify.logPosition("bottom right");
            alertify.success("Thank you, the details submitted will be checked and one of our team will take the appropriate action.");

            $('.loading-show2').hide();
            $('#send_message').prop('disabled', false);

            $('#employer_name').val('');
            $('#employer_website').val('');
        }, 2000);


    })

